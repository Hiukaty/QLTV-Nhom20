    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed
        // TODO add your handling code here:
        String taiKhoan = this.txtTaiKhoan.getText();
        String matKhau = this.txtMatKhau.getText();
        if(taiKhoan.isEmpty() || matKhau.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập đẩy đủ thông tin", "Thông Báo", 1);
        }
        else {
            KhachHang kh = khachhangdata.dangNhap(taiKhoan, matKhau);
            Admin ad = admindata.dangNhap(taiKhoan, matKhau);
            try{
                if(kh!=null) {
                    JKhachHang jkh = new JKhachHang();
                    jkh.setVisible(true);
                    dispose();
                }
                else if(ad!=null) {
                    test t = new test();
                    t.setVisible(true);
                    dispose();
                }
                else JOptionPane.showMessageDialog(null, "Sai tài khoản hoặc mật khẩu!", "Thông báo", 1);
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
    }//GEN-LAST:event_btLoginActionPerformed
